          <h2 class="text-center font-black text-[38px] m-10">Visi Partai Nasdem</span></h2>
          <span>Visi adalah ‘cita-cita’. Hanya saja, cita-cita biasanya digunakan sebagai istilah keseharian yang kurang pantas bila digunakan dalam institusi resmi. Untuk institusi seperti partai politik, maka penggunakan nomeklatur yang tepat adalah visi. Asal katanya dari Bahasa Inggris, vision yang berarti ‘konsep tentang masa depan yang dianggap ideal’ (Oxford Dictionary). Demikian juga dengan ‘misi’ berasal dari Bahasa Inggris (mission) berarti: “an important official job that a person or group of people is given to do” (Oxford Dictionary). Artinya, “sebuah pekerjaan resmi dan penting untuk dilakukan oleh seseorang atau kelompok.” Jadi dengan kata lain, visi merupakan impian besar sedangkan misi adalah rumusan langkah untuk mencapai impian tersebut.</span>
